// Math Object

// 1.Math.round:
console.log(Math.round(75.4));

// output
// 75

// 2.Math.ceil:
console.log(Math.ceil(5.2));

// output
// 6

// 3.Math.floor:
console.log(Math.floor(6.7));

// output
// 6

// 4.Math.random:
console.log(Math.random()*12);

// output
// 5.637835764588513

// 5.Math.sqrt:
console.log(Math.sqrt(81));

// output
// 9

// Number Conversion:

// 1.parseInt:
let number = "2000";
console.log(parseInt(number));

// output
// 2000

// 2.parseFloat:
let  float = "89.7";
console.log(parseFloat(float));

// output
// 89.7
